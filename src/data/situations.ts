export type Situation = {
  business: string;
  hiringReason: string;
  desiredTrait: string;
};

function pickRandom<T>(arr: readonly T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function generateSituation(
  companyType: keyof typeof BUSINESSES
): Situation {
  return {
    business: pickRandom(BUSINESSES[companyType]),
    hiringReason: pickRandom(HIRING_REASONS[companyType]),
    desiredTrait: pickRandom(DESIRED_TRAITS[companyType]),
  };
}
